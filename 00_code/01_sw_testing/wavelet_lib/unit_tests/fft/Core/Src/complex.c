void print_complex_vector(complex *x, int n, int j)
{
  for(int i = 0; i < j; i++)
  {
    for(int k = 0; k < n; k++)
    {
      char str[24];
      sprintf(str, " %2.4f,%2.4fj ", x[(i*n) + k].Re, x[(i*n) + k].Im);
      UART_puts(str);      
    }
    UART_puts("\n\r");
  } 
  UART_puts("\n\r");
}